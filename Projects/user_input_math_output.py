num1 = float(input("What is the first number?\n"))
num2 = float(input("What is the second number?\n"))

print(str(num1) + " + " + str(num2) + " = " + str(num1 + num2))
print(str(num1) + " - " + str(num2) + " = " + str(num1 - num2))
print(str(num1) + " / " + str(num2) + " = " + str(num1 / num2))
print(str(num1) + " * " + str(num2) + " = " + str(num1 * num2))
print(str(num1) + " ^ " + str(num2) + " = " + str(num1 ** num2))
print(str(num1) + " % " + str(num2) + " = " + str(num1 % num2))